package Java_seminar.seminar_3.cw2;

import java.util.List;

public class Student {
    public String name;
    public int number;
    public double salary;
    public List<Integer> balls;


    public Student(String name, int number, double salary, List<Integer> balls) {
        this.name = name;
        this.number = number;
        this.salary = salary;
        this.balls = balls;
    }
}
